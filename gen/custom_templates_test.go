func testSchema() *schema.Schema {
	return &schema.Schema{
		Tables: []*schema.Table{
			{
				Name: "users",
				Columns: []*schema.Column{
					{Name: "id", DBType: "integer", IsAutoIncrement: true, HasDefault: true},
					{Name: "name", DBType: "text"},
				},
				PrimaryKey: &schema.PrimaryKey{Columns: []string{"id"}},
			},
		},
	}
}

func TestCustomTemplateOverride(t *testing.T) {
	t.Parallel()

	// Create a temp dir with a custom model.go.tmpl that adds a marker comment.
	tmplDir := t.TempDir()
	// Read the real model template, then inject a marker comment.
	realTmpl, err := templateFS.ReadFile("templates/model.go.tmpl")
	if err != nil {
		t.Fatal(err)
	}
	customModel := strings.Replace(string(realTmpl),
		"// Code generated by sqlgen. DO NOT EDIT.",
		"// Code generated by sqlgen. DO NOT EDIT.\n// CUSTOM_TEMPLATE_MARKER",
		1)

	if err = os.WriteFile(filepath.Join(tmplDir, "model.go.tmpl"), []byte(customModel), 0o644); err != nil {
		t.Fatal(err)
	}

	s := testSchema()
	cfg := &config.Config{
		Input:  config.InputConfig{Dialect: "postgres", Paths: []string{"x"}},
		Output: config.OutputConfig{Templates: tmplDir},
		Types:  config.TypesConfig{NullType: config.NullTypeGeneric},
	}
	g := NewGenerator(cfg, s)
	outDir := generateAndBuild(t, g, "customoverride")

	// Verify the marker is in the output.
	model, err := os.ReadFile(filepath.Join(outDir, "sqlgen_users_model.go"))
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(model), "CUSTOM_TEMPLATE_MARKER") {
		t.Error("expected custom template marker in generated model")
	}
}

func TestExtraTemplate(t *testing.T) {
	t.Parallel()

	tmplDir := t.TempDir()
	extraTmpl := `{{- $tableName := .Table.Name -}}
{{- $structName := pascal (singular $tableName) -}}
// Code generated by sqlgen. DO NOT EDIT.

package {{.Package}}

// {{$structName}}Extra is a custom extra type.
type {{$structName}}Extra struct {
	TableName string
}

func New{{$structName}}Extra() *{{$structName}}Extra {
	return &{{$structName}}Extra{TableName: "{{$tableName}}"}
}
`
	if err := os.WriteFile(filepath.Join(tmplDir, "extra.go.tmpl"), []byte(extraTmpl), 0o644); err != nil {
		t.Fatal(err)
	}

	s := testSchema()
	cfg := &config.Config{
		Input:  config.InputConfig{Dialect: "postgres", Paths: []string{"x"}},
		Output: config.OutputConfig{Templates: tmplDir},
		Types:  config.TypesConfig{NullType: config.NullTypeGeneric},
	}
	g := NewGenerator(cfg, s)
	outDir := generateAndBuild(t, g, "extratemplate")

	// Verify the extra file was created.
	extraFile := filepath.Join(outDir, "sqlgen_users_extra.go")
	content, err := os.ReadFile(extraFile)
	if err != nil {
		t.Fatalf("extra template file not generated: %v", err)
	}
	if !strings.Contains(string(content), "UserExtra") {
		t.Error("expected UserExtra struct in extra template output")
	}
}
